package java_basics;

public class Variables_And_Datatypes {

	public static void main(String[] args) {
		// Java has primitive data types: int, double, char, float, boolean, long, short, byte
		/*Variables: containers for data types.
		 In simple words, Variables are used to store the data types*/
		
		int age = 28;
		double decimal_number = 445.34;
		char grade = 'A';
		boolean isQA = true;
		
		System.out.println("The age is " + age);
		System.out.println("the decimal number is " + decimal_number);
		System.out.println("The secured grade is " + grade);
		System.out.println("is a QA? " + isQA);

	}

}

